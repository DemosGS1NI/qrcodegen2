<section class="prose prose-sm max-w-none text-gray-800">
  <p>
    Los codigos QR impulsados por GS1 pueden proporcionar a varios usuarios, incluidos los actores de la cadena de suministro,
    cajeros, reguladores y consumidores, acceso a informacion critica sobre los productos. Estas directrices solo son aplicables
    a los codigos QR impulsados por GS1 (con GS1 Digital Link dentro) basados en los estandares de GS1.
  </p>

  <ul>
    <li>Estos codigos QR son diferentes de cualquier otro codigo QR y pueden conectarse a datos enriquecidos.</li>
    <li>Pueden ser escaneados en el punto de venta (POS) y por telefonos o apps para obtener informacion del producto en linea.</li>
  </ul>

  <h3>Transicion - codigo lineal y QR en el paquete</h3>
  <ul>
    <li>EAN-13: ancho de la barra mas delgada igual a 0.33 mm</li>
    <li>Codigo QR: cuadrado mas pequeno igual a 0.495 mm</li>
    <li>Altura minima de la fuente para el texto GS1: 2 mm</li>
    <li>GTIN no incluido debajo del codigo QR</li>
  </ul>
  <img src="/guidelines/transition.png" alt="Transicion codigo QR" class="rounded border border-gray-200" />

  <h3>Ambicion 2027 - solo codigo QR en el paquete</h3>
  <ul>
    <li>Dimension X del codigo QR: 0.495 mm</li>
    <li>Altura minima del texto GS1 y GTIN: 2 mm</li>
    <li>GTIN incluido debajo del codigo QR</li>
  </ul>
  <img src="/guidelines/ambition.png" alt="Ambicion 2027" class="rounded border border-gray-200" />

  <h3>Zona de Silencio</h3>
  <p>Debe ser 4 veces la dimension X en todos los lados del codigo QR.</p>
  <img src="/guidelines/font_size_placement.png" alt="Zona de silencio" class="rounded border border-gray-200" />

  <h3>Ubicacion de transicion del codigo QR para POS</h3>
  <p>
    Los codigos QR que estan destinados a ser escaneados en el POS deben colocarse dentro de los 50 mm del centro del codigo de barras
    lineal para lograr el objetivo de articulos escaneados por minuto (IPM).
  </p>
  <img src="/guidelines/transition_for_pos_max_distance.png" alt="Ubicacion transicion POS" class="rounded border border-gray-200" />

